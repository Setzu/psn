DROP TABLE trophies;

CREATE TABLE IF NOT EXISTS trophies (
  id INT PRIMARY KEY NOT NULL AUTO_INCREMENT,
  id_psn INT NOT NULL,
  trophy_name VARCHAR(255) NOT NULL,
  trophy_icon VARCHAR(255) NOT NULL,
  game VARCHAR(255) NOT NULL,
  platform CHAR(15) NOT NULL,
  trophy_type VARCHAR(8) NOT NULL,
  date_obtention TIMESTAMP NOT NULL,
  date_insertion TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP
) DEFAULT CHARSET=utf8 COLLATE=utf8_general_ci;
