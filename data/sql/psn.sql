DROP TABLE psn;

CREATE TABLE IF NOT EXISTS psn (
  id INT PRIMARY KEY NOT NULL AUTO_INCREMENT,
  user_id INT NOT NULL UNIQUE,
  psn_id VARCHAR(255) NOT NULL UNIQUE,
  country CHAR(5) NOT NULL DEFAULT 'fr',
  avatar_xl VARCHAR(255) NOT NULL DEFAULT '',
  avatar_m VARCHAR(255) NOT NULL DEFAULT '',
  friends_count INT NOT NULL DEFAULT 0,
  trophy_level INT NOT NULL DEFAULT 0,
  progress INT NOT NULL DEFAULT 0,
  bronze INT NOT NULL DEFAULT 0,
  silver INT NOT NULL DEFAULT 0,
  gold INT NOT NULL DEFAULT 0,
  platinum INT NOT NULL DEFAULT 0,
  date_creation TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  last_update TIMESTAMP DEFAULT CURRENT_TIMESTAMP
) DEFAULT CHARSET=utf8 COLLATE=utf8_general_ci;
